#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*s;
	int		i;

	i = 0;
	s = (char *)malloc(sizeof(char) * ft_strlen(src) + 1);
	if (!s)
		return (NULL);
	while (src[i] != '\0')
	{
		s[i] = src[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
